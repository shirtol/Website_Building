export class Student {
    constructor({
        id,
        firstName,
        lastName,
        capsule,
        age,
        city,
        gender,
        hobby,
    }) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.capsule = capsule;
        this.age = age;
        this.city = city;
        this.gender = gender;
        this.hobby = hobby;
    }
}
